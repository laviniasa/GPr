# A função isalnum() na linguagem de programação Python verifica se todos os caracteres em
#  uma determinada string são alfanuméricos ou não.

# O isalnum()método retorna True se todos os caracteres forem alfanuméricos, ou seja, letra do alfabeto (az) e números (0-9).

# Exemplo de caracteres que não são alfanuméricos: (espaço)!#%&? etc.

# string.isalnum()


txt = "Company12"

x = txt.isalnum()

print(x)

# true