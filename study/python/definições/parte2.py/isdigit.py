# Em Python, isdigit() é um método embutido usado para manipulação de strings. 
# O método isdigit() retorna “Verdadeiro” se todos os caracteres na string forem dígitos, caso contrário, retorna “Falso”. 
# Esta função é usada para verificar se o argumento contém dígitos como: 0123456789

string = '15460'
print(string.isdigit())
  
 
string = '154ayush60'
print(string.isdigit())

# Verdade
# Falso