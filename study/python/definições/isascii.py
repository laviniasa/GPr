# isascii() checa se a String é um ASCII. Retorna true se for um ASCII, caso contrário retorna false.
# 0 A mesma lógica para os outros dois métodos, mas claro, cada um checa algo diferente.

# ASCII significa "American Standard Code for Information Interchange".

# Ele foi projetado no início dos anos 60, como um conjunto de caracteres padrão para computadores e dispositivos eletrônicos.

# ASCII é um conjunto de caracteres de 7 bits contendo 128 caracteres.

# Ele contém os números de 0 a 9, as letras inglesas maiúsculas e minúsculas de A a Z e alguns caracteres especiais.

# Os conjuntos de caracteres usados ​​em computadores modernos, em HTML e na Internet, são todos baseados em ASCII.

# As tabelas a seguir listam os 128 caracteres ASCII e seus números equivalentes.

# Sintaxe
# string.isascii()

txt = "Company123"

x = txt.isascii()

