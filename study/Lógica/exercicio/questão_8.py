# Faça uma função que recebe um valor inteiro e verifica se o valor é par ou ímpar. 
# A função deve retornar um valor booleano

# - definir uma função 'm' com um numero inteiro 'n'como parametro 

# - criar uma condição if onde se o resto de 'n' dividido por dois é igual a 0 

# - retorna o valor booleano True

# - se não retorna False


def m(n):
    if n % 2 == 0:
        return True
    else:
        return False
