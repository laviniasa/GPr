# Crie uma função que dado um número inteiro n retorna o somatorio dos números de 0 a n

# - definir uma funçao 'teste' com um numero n como parametro

# - criar uma variavel soma = 0 

# - criar um laço de loop for onde i é a variavel interavel ultilizando a função range
#  que permite especificar o inicio e o valor final 

# - atualizar variavel soma onde soma é igual a soma + i

# -retornar soma

def Teste(n):
    soma = 0
    for i in range (0, n):
        soma = soma + i
    return soma

















